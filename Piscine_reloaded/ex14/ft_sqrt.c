int	ft_sqrt(int nb)
{
	int mul;

	mul = 0;
	if (nb <= 0 || nb >= 2147483647)
		return (0);
	while ((mul * mul) < nb && mul <= 46341)
		mul++;
	if ((mul * mul) == nb)
		return (mul);
	return (0);
}
