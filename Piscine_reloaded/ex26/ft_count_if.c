int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int compteur;

	i = 0;
	compteur = 0;
	while (tab[i] != '\0')
		if (f(tab[i++]) == 1)
			compteur++;
	return (compteur);
}
